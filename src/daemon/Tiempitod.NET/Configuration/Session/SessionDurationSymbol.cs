namespace Tiempitod.NET.Configuration.Session;

/// <summary>
/// Represents the symbols for session duration.
/// </summary>
public enum SessionDurationSymbol
{
    /// <summary>
    /// Represents a duration in milliseconds.
    /// </summary>
    Millisecond,
    
    /// <summary>
    /// Represents a duration in seconds.
    /// </summary>
    Second,
    
    /// <summary>
    /// Represents a duration in minutes.
    /// </summary>
    Minute,
    
    /// <summary>
    /// Represents a duration in hours.
    /// </summary>
    Hour
}
